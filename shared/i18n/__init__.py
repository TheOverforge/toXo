def tr(key: str, **kwargs) -> str:
    """Return translated string for current language. Falls back to RU, then key."""
    text = _T.get(_lang, _T["ru"]).get(key) or _T["ru"].get(key, key)
    return text.format(**kwargs) if kwargs else text


def set_language(lang: str) -> None:
    global _lang
    if lang in _T:
        _lang = lang


def current_language() -> str:
    return _lang
